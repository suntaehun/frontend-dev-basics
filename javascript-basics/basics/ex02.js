/*
변수와 자료형(Data Type)

[기본타입(Primitive Type)]
- undefined
- number
- string
- boolean
[객체]

*/
var u = undefined; // var u; 동일, 선언 정의가 구분이 없다.
var i = 10;
var s = "Hello World";
var b = true;

console.log(u + " : " + typeof(u));
console.log(i + " : " + typeof(i));
console.log(s + " : " + typeof(s));
console.log(b + " : " + typeof(b));
console.log(s.length);
console.log(s.toUpperCase());
